% Construct the order 13 matrix
%     /  -10   2    0   ...  \
% K = |   1   -10  ...    0  |.
%     |   0   ...   -10   2  |
%     \  ...   0    1   -10  /

n = 13;
K = -10 * eye(n) + diag(2 * ones(1, n-1), 1) + diag(ones(1, n-1), -1);

